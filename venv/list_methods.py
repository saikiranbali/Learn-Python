numbers = [1,2,3,4,5,6]
print(numbers)
numbers.append(7) #appends the value at the end of list
print(numbers)
numbers.insert(2,88) #(index,value) inserts given number at given index
print(numbers) 
numbers.remove(1) #removes the value 1 from list
print(numbers)
print(3 in numbers) #returns true if 3 is present in list else false
print(len(numbers)) #returns size of the llist